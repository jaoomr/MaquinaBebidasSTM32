void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim){
	if(htim == &htim3) HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);
	if(htim == &htim4){
		tm.tm_sec++;
		if(tm.tm_sec>=60){
			tm.tm_sec=0;
			tm.tm_min+=1;
		}
		if(tm.tm_min>=60){
			tm.tm_min=0;
			tm.tm_hour+=1;
		}
		if(tm.tm_hour>=24){
			tm.tm_hour=0;
		}
	}
}

